(ns build
  "Build + deploy for dbos-clj.

  Mirrors the kmono workspace build (tools.build jar with a generated pom,
  deployed to Clojars via kaven), scoped to this single package. The version
  is derived from the latest `vMAJOR.MINOR.PATCH` git tag, the same scheme
  kmono uses.

    clojure -T:build build     ; jar -> target/dbos-clj-<version>.jar
    clojure -T:build release   ; build, then deploy to Clojars
    clojure -T:build clean

  `release` reads Clojars credentials from the CLOJARS_USERNAME /
  CLOJARS_PASSWORD environment variables."
  (:require
   [clojure.string :as str]
   [clojure.tools.build.api :as b]
   [k16.kaven.deploy :as kaven.deploy]))

(def lib 'com.shipclojure/dbos-clj)

(def ^:private class-dir "target/classes")
(def ^:private src-dirs ["src"])

(defn- git-tag-version
  "The version from the latest `v*` git tag (leading `v` stripped), or nil
  when there is no matching tag / git is unavailable."
  []
  (try
    (let [{:keys [exit out]} (b/process {:command-args ["git" "describe" "--tags"
                                                        "--abbrev=0" "--match" "v*"]
                                         :out :capture
                                         :err :capture})]
      (when (zero? exit)
        (second (re-matches #"v(.*)" (str/trim out)))))
    (catch Exception _ nil)))

(defn- resolve-version [version]
  (or version
      (git-tag-version)
      (throw (ex-info (str "No version: pass :version or create a `v*` git tag "
                           "(e.g. `git tag v0.1.0`).")
                      {:lib lib}))))

(defn- jar-file [version]
  (format "target/%s-%s.jar" (name lib) version))

(defn clean
  "Delete the target directory."
  [_]
  (b/delete {:path "target"}))

(defn build
  "Build target/dbos-clj-<version>.jar with a generated pom. `:version`
  defaults to the latest `v*` git tag. Returns the resolved version."
  [{:keys [version]}]
  (clean nil)
  (let [version (resolve-version version)
        basis (b/create-basis {:project "deps.edn"})]
    (b/copy-dir {:src-dirs src-dirs
                 :target-dir class-dir})
    (b/write-pom {:class-dir class-dir
                  :lib lib
                  :version version
                  :basis basis
                  :src-dirs src-dirs
                  :pom-data [[:description
                              "A Clojure wrapper for DBOS Transact durable execution."]
                             [:url "https://github.com/shipclojure/dbos-clj"]
                             [:licenses
                              [:license
                               [:name "MIT"]
                               [:url "https://opensource.org/license/mit"]]]]})
    (b/jar {:class-dir class-dir
            :jar-file (jar-file version)})
    (println "Wrote" (jar-file version))
    version))

(def ^:private clojars-credentials
  {:username (System/getenv "CLOJARS_USERNAME")
   :password (System/getenv "CLOJARS_PASSWORD")})

(defn release
  "Build the jar and deploy it to Clojars via kaven. The clojars repository
  URL is read from the pom generated by `build`; credentials come from the
  CLOJARS_USERNAME / CLOJARS_PASSWORD env vars."
  [opts]
  (let [version (build opts)]
    (kaven.deploy/deploy
     {:jar-path (jar-file version)
      :repository {:id "clojars"
                   :credentials clojars-credentials}})
    (println "Deployed" lib version "to Clojars")))
